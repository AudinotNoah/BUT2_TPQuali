public class Position {

    private int x;
    private int y;

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

    public void setX(int pX){
        x = pX;
    }

    public void setY(int pY){
        y = pY;
    }

    public void setAll(int pX, int pY){
        x = pX;
        y = pY;
    }
}
